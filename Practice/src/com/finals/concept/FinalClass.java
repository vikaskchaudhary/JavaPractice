package com.finals.concept;

public final class FinalClass {
	private final int intVar;
	private final String  strVar;
	public FinalClass(int intVar, String strVar){
		this.intVar=intVar;
		this.strVar=strVar;
	}
	public int getIntVar() {
		return intVar;
	}
	public String getStrVar() {
		return strVar;
	}
	
	

}
